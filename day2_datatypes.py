# Numbers
a = 10              # int
b = 3.14            # float
c = 2 + 3j          # complex number

print("a:", a, type(a))
print("b:", b, type(b))
print("c:", c, type(c))

# Booleans
is_sunny = True
is_raining = False
print("is_sunny:", is_sunny, type(is_sunny))

# Strings
greeting = "Hello, World!"
name = "Leon"
print(greeting + " My name is " + name)

# Lists
fruits = ["apple", "banana", "cherry"]
fruits.append("date")
print("fruits:", fruits)

# Tuples
coordinates = (10.0, 20.0)
print("coordinates:", coordinates)

# Dictionaries
person = {"name": "Leon", "age": 23, "student": True}
print("person name:", person["name"])
print("person age:", person["age"])

# Sets
unique_numbers = {1, 2, 3, 3, 2, 1}
print("unique_numbers:", unique_numbers)
